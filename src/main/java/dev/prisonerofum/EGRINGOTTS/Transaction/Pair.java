package dev.prisonerofum.EGRINGOTTS.Transaction;

// Utility class to represent a pair of elements
class Pair<T, U> {
    private final T first;
    private final U second;

    public Pair(T first, U second) {
        this.first = first;
        this.second = second;
    }

    public T getFirst() {
        return first;
    }

    public U getSecond() {
        return second;
    }
}
